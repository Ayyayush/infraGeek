/**
 * =========================================================
 * AWS (AMAZON WEB SERVICES)
 * =========================================================
 *
 * WHAT IS AWS?
 * ---------------------------------------------------------
 * AWS ka full form hai:
 * Amazon Web Services
 *
 * AWS ek cloud computing platform hai jo
 * internet ke through on-demand IT resources
 * provide karta hai.
 *
 * Simple words me:
 * - Internet ke through servers, storage, databases,
 *   networking aur aur bhi bahut saari services milti hain
 * - Physical hardware kharidne ya maintain karne
 *   ki koi zarurat nahi hoti
 *
 *
 * ---------------------------------------------------------
 * CORE CONCEPT
 * ---------------------------------------------------------
 *
 * - On-demand access to IT resources
 * - No physical hardware required
 * - Pay-as-you-go pricing model
 *
 * Matlab:
 * Jitna resource use karo, utna hi paisa pay karo.
 *
 *
 * ---------------------------------------------------------
 * FEATURES OF AWS
 * ---------------------------------------------------------
 *
 * 1) SCALABILITY
 *    - Resources ko easily scale up ya scale down kar sakte ho
 *    - Traffic badhne par auto-scaling possible hai
 *
 *
 * 2) SECURITY
 *    - IAM (users, roles, permissions)
 *    - Encryption, firewalls, monitoring
 *    - Shared Responsibility Model follow hota hai
 *
 *
 * 3) COST EFFECTIVENESS
 *    - No upfront hardware cost
 *    - Pay only for what you use
 *    - Multiple pricing options available
 *
 *
 * 4) HIGH AVAILABILITY
 *    - Multiple Availability Zones
 *    - Fault-tolerant infrastructure
 *
 *
 * 5) DISASTER RECOVERY
 *    - Data backups aur replication
 *    - Multi-region recovery support
 *
 *
 * ---------------------------------------------------------
 * COMMON AWS SERVICES
 * ---------------------------------------------------------
 *
 * 1) EC2 (Elastic Compute Cloud)
 *    - Virtual servers in the cloud
 *    - Applications aur backend run karne ke liye
 *
 *
 * 2) S3 (Simple Storage Service)
 *    - Object storage service
 *    - Files, images, videos, backups store karne ke liye
 *
 *
 * 3) VPC (Virtual Private Cloud)
 *    - AWS ke andar private network
 *    - Secure communication ke liye
 *
 *
 * 4) IAM (Identity and Access Management)
 *    - Users, roles aur permissions manage karta hai
 *    - AWS security ka backbone hai
 *
 *
 * 5) RDS (Relational Database Service)
 *    - Managed relational databases
 *    - MySQL, PostgreSQL, SQL Server, etc.
 *
 *
 * ---------------------------------------------------------
 * IMPORTANT AWS TERMINOLOGIES
 * ---------------------------------------------------------
 *
 * 1) REGION
 *    - AWS ka geographical area
 *    - Example: Mumbai (ap-south-1), US-East-1
 *
 *
 * 2) AVAILABILITY ZONE (AZ)
 *    - Ek region ke andar multiple isolated data centers
 *    - High availability aur fault tolerance ke liye
 *
 *
 * 3) EDGE LOCATION
 *    - End users ke paas located data centers
 *    - Mostly CDN (CloudFront) ke liye use hote hain
 *    - Fast content delivery ke liye
 *
 *
 * ---------------------------------------------------------
 * INTERVIEW FRIENDLY SUMMARY
 * ---------------------------------------------------------
 *
 * AWS is a cloud computing platform that provides
 * scalable, secure, and cost-effective IT resources
 * over the internet using a pay-as-you-go model.
 *
 */