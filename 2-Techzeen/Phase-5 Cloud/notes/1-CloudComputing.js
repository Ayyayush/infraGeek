/**
 * =========================================================
 * CLOUD COMPUTING
 * =========================================================
 *
 * WHAT IS CLOUD COMPUTING?
 * ---------------------------------------------------------
 * Cloud computing ek model hai jisme hum
 * internet ke through computing services use karte hain.
 *
 * Simple words me:
 * Internet ke through servers, storage, databases,
 * networking, software wagaira use karna
 * bina apna physical hardware kharide.
 *
 * Cloud providers:
 * - Amazon Web Services (AWS)
 * - Microsoft Azure
 * - Google Cloud Platform (GCP)
 *
 *
 * ---------------------------------------------------------
 * BASIC DEFINITION (INTERVIEW FRIENDLY)
 * ---------------------------------------------------------
 *
 * Cloud computing provides on-demand access to
 * computing resources over the internet with
 * pay-as-you-go pricing.
 *
 *
 * ---------------------------------------------------------
 * CLOUD INFRASTRUCTURE
 * ---------------------------------------------------------
 *
 * Cloud infrastructure physical hardware par based hoti hai,
 * lekin user ko ye:
 * - Virtualized servers ke form me milti hai
 *
 * Isme include hota hai:
 * - Virtual Machines (VMs)
 * - Storage (Block, Object)
 * - Networking (VPC, Load Balancer)
 *
 * Key features:
 * - Virtualized servers
 * - Pay only for what you use
 * - Auto-scaling & instant resource provisioning
 * - High availability & redundancy
 *
 *
 * ---------------------------------------------------------
 * TRADITIONAL INFRASTRUCTURE (ON-PREMISE)
 * ---------------------------------------------------------
 *
 * Traditional setup me:
 * - Physical servers khud kharidne padte hain
 * - Data center manage karna padta hai
 * - Maintenance aur upgrades manual hote hain
 *
 * Problems:
 * - High upfront cost
 * - Limited scalability
 * - Slow provisioning
 *
 *
 * ---------------------------------------------------------
 * CLOUD vs TRADITIONAL (KEY COMPARISON)
 * ---------------------------------------------------------
 *
 * 1) SCALABILITY
 *    Cloud:
 *    - Auto scaling
 *    - Resources instantly badha / ghata sakte hain
 *
 *    Traditional:
 *    - Hardware limited
 *    - Manual scaling
 *
 *
 * 2) COST EFFECTIVENESS
 *    Cloud:
 *    - Pay-as-you-go
 *    - No upfront hardware cost
 *
 *    Traditional:
 *    - Heavy initial investment
 *    - Idle resources ka cost
 *
 *
 * 3) SECURITY
 *    Cloud:
 *    - Shared responsibility model
 *    - Advanced security tools
 *
 *    Traditional:
 *    - Security poori tarah company ki responsibility
 *
 *
 * 4) FLEXIBILITY
 *    Cloud:
 *    - Multiple services & regions
 *    - Easy integration
 *
 *    Traditional:
 *    - Hardware dependent
 *
 *
 * 5) DISASTER RECOVERY
 *    Cloud:
 *    - Built-in backups
 *    - Multi-region recovery
 *
 *    Traditional:
 *    - Expensive & complex
 *
 *
 * ---------------------------------------------------------
 * TYPES OF CLOUD
 * ---------------------------------------------------------
 *
 * 1) PUBLIC CLOUD
 *    - Infrastructure cloud provider ki hoti hai
 *    - Resources multiple customers share karte hain
 *    - Example: AWS, Azure, GCP
 *
 *
 * 2) PRIVATE CLOUD
 *    - Infrastructure single organization ke liye
 *    - On-premise ya dedicated cloud ho sakta hai
 *    - More control & security
 *
 *
 * 3) HYBRID CLOUD
 *    - Public + Private cloud ka combination
 *    - Sensitive data private me
 *    - Normal workloads public me
 *
 *
 * ---------------------------------------------------------
 * SHORT SUMMARY
 * ---------------------------------------------------------
 *
 * Cloud computing:
 * - Internet ke through computing resources
 * - No physical hardware management
 * - Scalable, cost-effective & flexible
 *
 * Public, Private & Hybrid cloud
 * industry me widely used models hain.
 *
 */