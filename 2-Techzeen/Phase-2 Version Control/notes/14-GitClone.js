/**
 * =========================================================
 * GIT CLONE COMMAND
 * =========================================================
 *
 * Git Clone kya hota hai?
 * ------------------------
 * Git clone ek Git command hai jiska use
 * remote repository (GitHub / GitLab / Bitbucket)
 * se poora project apne local system me copy
 * karne ke liye kiya jata hai.
 *
 *
 * ---------------------------------------------------------
 * NEED (Git Clone ki zarurat kyun hoti hai?)
 * ---------------------------------------------------------
 *
 * - Jab hume kisi existing project pe kaam start karna ho
 * - Jab team ke kisi aur member ka code local system me lana ho
 * - Jab open-source project ko explore ya contribute karna ho
 *
 *
 * ---------------------------------------------------------
 * BENEFITS (Faayde)
 * ---------------------------------------------------------
 *
 * - Poora project ek command me mil jata hai
 * - Complete commit history ke saath code aata hai
 * - Team collaboration easy ho jata hai
 * - Version control ke saare features available rehte hain
 *
 *
 * ---------------------------------------------------------
 * WHY TO USE GIT CLONE?
 * ---------------------------------------------------------
 *
 * - Remote repo aur local repo sync me rehte hain
 * - Changes track karna easy hota hai
 * - Branching aur merging smoothly kaam karta hai
 * - Production aur development workflow easy ban jata hai
 *
 *
 * ---------------------------------------------------------
 * BASIC SYNTAX
 * ---------------------------------------------------------
 *
 * git clone <repository-url>
 *
 *
 * ---------------------------------------------------------
 * BASIC COMMAND
 * ---------------------------------------------------------
 *
 * Example (HTTPS):
 * git clone https://github.com/username/repository-name.git
 *
 * Example (SSH):
 * git clone git@github.com:username/repository-name.git
 *
 *
 * ---------------------------------------------------------
 * VARIOUS FORMS OF GIT CLONE COMMAND
 * ---------------------------------------------------------
 *
 * 1) Clone into a specific folder
 *    git clone <repo-url> <folder-name>
 *
 *    Example:
 *    git clone https://github.com/user/app.git myApp
 *
 *
 * 2) Clone a specific branch only
 *    git clone -b <branch-name> <repo-url>
 *
 *    Example:
 *    git clone -b develop https://github.com/user/app.git
 *
 *
 * 3) Shallow clone (latest commits only)
 *    git clone --depth=1 <repo-url>
 *
 *    Example:
 *    git clone --depth=1 https://github.com/user/app.git
 *
 *
 * 4) Clone using SSH (recommended for contributors)
 *    git clone git@github.com:user/app.git
 *
 *
 * 5) Clone without checkout
 *    git clone --no-checkout <repo-url>
 *
 *
 * ---------------------------------------------------------
 * SHORT SUMMARY
 * ---------------------------------------------------------
 *
 * Git clone:
 * - Remote repository ko local system me copy karta hai
 * - Code + commit history dono milti hai
 * - Team aur open-source projects ke liye essential command
 *
 */